# Simple function
def greet():
    print("Hello from the function!")

# Function with parameters
def greet_person(name):
    print(f"Hello {name}!")

# Function that returns a value
def add(a, b):
    return a + b

# Use the functions
greet()
greet_person("Anna")
result = add(5, 3)
print(f"5 + 3 = {result}")

