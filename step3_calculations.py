# Basic math
a = 10
b = 5

sum_result = a + b
difference = a - b
product = a * b
quotient = a / b

print(f"Sum: {a} + {b} = {sum_result}")
print(f"Difference: {a} - {b} = {difference}")
print(f"Product: {a} * {b} = {product}")
print(f"Quotient: {a} / {b} = {quotient}")

