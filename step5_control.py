# If statements
age = int(input("How old are you? "))

if age >= 18:
    print("You are an adult!")
else:
    print("You are a minor!")

# Loops
print("\nCounting from 1 to 5:")
for i in range(1, 6):
    print(i)

# While loop
counter = 0
while counter < 3:
    print(f"Counter: {counter}")
    counter += 1

